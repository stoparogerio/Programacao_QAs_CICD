console.log(1 + 1); // 2
console.log(10 - 7); // 3
console.log(2 * 2); // 4

console.log(11 / 2);
console.log(11 % 2);

console.log(4 ** 4);

console.log(2 + 3 * 4); //  precedencia - multiplicao, adicao
console.log((2 + 3) * 4); //  precedência - dentro do parenteses, depois o que está fora

